/**
 * Extracts the shared WordPress/Elementor CSS boilerplate from an existing page.tsx
 * and writes it to src/lib/wpBoilerplate.ts
 * Usage: node extract-boilerplate.mjs
 */
import { readFileSync, writeFileSync } from 'fs';
import { fileURLToPath } from 'url';
import path from 'path';

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const srcPage = path.join(__dirname, 'src/app/biografia/page.tsx');

const raw = readFileSync(srcPage, 'utf8');

// The dangerouslySetInnerHTML content starts after the first ` in __html: `
const htmlStart = raw.indexOf('__html: `') + '__html: `'.length;
const fullHtml = raw.slice(htmlStart);

// The shared CSS ends with global.css; the NEXT link is the page-specific one.
const globalCssLine = '<link rel="stylesheet" id="elementor-global-css" href="/css/global.css" media="all">';
const globalCssIdx = fullHtml.indexOf(globalCssLine);
if (globalCssIdx === -1) {
  console.error('Cannot find elementor-global-css link');
  process.exit(1);
}

// boilerplateBefore includes global.css itself
const boilerplateBefore = fullHtml.slice(0, globalCssIdx + globalCssLine.length);
const afterGlobal = fullHtml.slice(globalCssIdx + globalCssLine.length);

// The line immediately after global.css is the page-specific elementor CSS
const postCssMatch = afterGlobal.match(/<link rel="stylesheet" id="elementor-post-\d+-css" href="\/css\/(post-\d+\.css)" media="all">/);
if (!postCssMatch) {
  console.error('Cannot find page-specific post CSS after global.css');
  process.exit(1);
}

const afterPostCss = afterGlobal.slice(afterGlobal.indexOf(postCssMatch[0]) + postCssMatch[0].length);

// Find where the actual HTML begins (the <header class="header"> or similar structural HTML)
// Look for the Neve header wrapper start
const headerStart = afterPostCss.search(/<div class="global-styled|<div id="neve-off-canvas|<div class="wrapper"|<header class="header"/);
let boilerplateAfter;
let htmlContent;
if (headerStart !== -1) {
  boilerplateAfter = afterPostCss.slice(0, headerStart);
  htmlContent = afterPostCss.slice(headerStart);
} else {
  // Fallback: find first <div class="neve- or <header
  const fallback = afterPostCss.search(/<header\b|<div class="neve-main/);
  boilerplateAfter = afterPostCss.slice(0, fallback);
  htmlContent = afterPostCss.slice(fallback);
}

console.log('boilerplateBefore length:', boilerplateBefore.length);
console.log('boilerplateAfter length:', boilerplateAfter.length);
console.log('htmlContent start:', htmlContent.slice(0, 80));

// Escape backticks and ${} in the CSS strings for use in template literals
function escape(s) {
  return s.replace(/\\/g, '\\\\').replace(/`/g, '\\`').replace(/\$\{/g, '\\${');
}

const output = `// AUTO-GENERATED by extract-boilerplate.mjs - do not edit manually
// Shared WordPress/Elementor CSS boilerplate injected into every page.

export const WP_BOILERPLATE_BEFORE = \`${escape(boilerplateBefore)}\`;

export const WP_BOILERPLATE_AFTER = \`${escape(boilerplateAfter)}\`;

/**
 * Builds the full CSS header string for a WordPress-style page.
 * @param postCssFiles - Page-specific CSS file names (e.g. ['post-60.css'])
 */
export function wpHead(postCssFiles: string[] = []): string {
  const postCssLinks = postCssFiles
    .map((f) => \`<link rel="stylesheet" href="/css/\${f}" media="all">\`)
    .join('\\n');
  return WP_BOILERPLATE_BEFORE + postCssLinks + WP_BOILERPLATE_AFTER;
}
`;

const outPath = path.join(__dirname, 'src/lib/wpBoilerplate.ts');
writeFileSync(outPath, output, 'utf8');
console.log('Written to', outPath);
